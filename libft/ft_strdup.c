#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	int		j;
	char	*s2;

	j = 0;
	i = ft_strlen(src);
	s2 = malloc((i + 1) * sizeof(char));
	if (!(s2))
		return (0);
	while (j < i)
	{
		s2[j] = src[j];
		j++;
	}
	s2[j] = '\0';
	return (s2);
}
